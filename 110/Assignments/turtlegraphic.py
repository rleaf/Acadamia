import turtle

t = turtle.Turtle()

t.hideturtle()
t.begin_fill()

t.forward(100)
t.right(90)
t.forward(100)
t.left(90)
t.forward(150)
t.right(90)
t.forward(100)
t.right(90)
t.forward(250)
t.right(90)
t.forward(200)
t.fillcolor('#4b99e0')
t.end_fill()

turtle.exitonclick()
